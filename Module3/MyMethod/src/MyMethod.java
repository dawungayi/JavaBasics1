/*
This code shows us how to implement Methods in Java,
Similar to Functions in C
 */

public class MyMethod {

    public static void main(String[] args) {
        System.out.println("Hello World");
        firstStep();
    }

    public static void firstStep() {
        // This is like a function in C
        int x = 10%2*3;
        String myStr = "My variable is: ";
        System.out.println(myStr +  x);
    }


}

